    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        // TODO add your handling code here:'       //to calculate price of pizza
        double sum = 0;
        if (pizzaTypeComboBox.getSelectedItem().equals("Thin Crust") && sizeComboBox.getSelectedItem().equals("Small")) {
            sum = sum + 3.99;
        } else if (pizzaTypeComboBox.getSelectedItem().equals("Deep Pan") && sizeComboBox.getSelectedItem().equals("Small")) {
            sum = sum + 5.49;
        } else if (pizzaTypeComboBox.getSelectedItem().equals("Thin Crust") && sizeComboBox.getSelectedItem().equals("Medium")) {
            sum = sum + 5.99;
        } else if (pizzaTypeComboBox.getSelectedItem().equals("Deep Pan") && sizeComboBox.getSelectedItem().equals("Medium")) {
            sum = sum + 7.99;
        } else if (pizzaTypeComboBox.getSelectedItem().equals("Thin Crust") && sizeComboBox.getSelectedItem().equals("Large")) {
            sum = sum + 8.99;
        } else if (pizzaTypeComboBox.getSelectedItem().equals("Deep Pan") && sizeComboBox.getSelectedItem().equals("Large")) {
            sum = sum + 11.99;
        }

        if (cheeseRadioButton.isSelected()) {
            sum = sum + 0.5;
        }
        if (sauceRadioButton.isSelected()) {
            sum = sum + 0.5;
        }
        if (beefCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (chickenCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (pepperoniCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (mushroomsCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (sweetCornCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (peppersCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (chiliCheckBox.isSelected()) {
            sum = sum + 0.5;
        }
        if (meatFeastCheckBox.isSelected()) {
            sum = sum + 1;
        }
        if (veggieSpecialCheckBox.isSelected()) {
            sum = sum + 1;
        }

        String finalSum = Double.toString(sum);
        sumTextField.setText(finalSum);

    }//GEN-LAST:event_calculateButtonActionPerformed

