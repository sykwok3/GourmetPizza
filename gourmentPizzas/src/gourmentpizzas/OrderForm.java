    private void newFormSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newFormSaveButtonActionPerformed
        // TODO add your handling code here:                                                         //code to validate post code
        Pattern postalCodePattern = Pattern.compile("BN1|BN2|BN3[0-9][a-z]{2}", Pattern.CASE_INSENSITIVE); //https://www.w3schools.com/java/java_regex.asp
        Matcher match = postalCodePattern.matcher(newPostCodeTextField.getText());                        
        boolean matchFound = match.find();
        if (!matchFound) {
            newPostCodeTextField.setText("Invalid postal code, please try again.");
        }
                                                                                  //code to validate phone number
        Pattern numberPattern = Pattern.compile("0[0-9]{10}", Pattern.CASE_INSENSITIVE); //https://www.w3schools.com/java/java_regex.asp
        Matcher match2 = numberPattern.matcher(newMobileNumTextField.getText());
        boolean matchFound2 = match2.find();
        if (!matchFound2) {
            newMobileNumTextField.setText("Invalid number please try again.");
        }

        if (matchFound && matchFound2) {              //code to save user input into text file
            try {
                FileWriter myWriter = new FileWriter("newOrders.txt", true);   
                myWriter.write("\n" + newNameTextField.getText() + ", " + newAddressTextField.getText() + ", " + newPostCodeTextField.getText() + ", " + newMobileNumTextField.getText()
                        + ", " + sizeComboBox.getSelectedItem() + ", " + pizzaTypeComboBox.getSelectedItem() + ", " + "Tomato(True)/BBQ Sauce(False): " + sauceRadioButton.isSelected()
                        + ", " + "Extra cheese(True)/No extra cheese(False): " + cheeseRadioButton.isSelected() + ", " + "Beef: " + beefCheckBox.isSelected() + ", " + "Chicken: " + chickenCheckBox.isSelected()
                        + ", " + "Pepperoni: " + pepperoniCheckBox.isSelected() + ", " + "Mushrooms: " + mushroomsCheckBox.isSelected() + ", " + "Sweetcorn: " + sweetCornCheckBox.isSelected()
                        + ", " + "Peppers: " + peppersCheckBox.isSelected() + ", " + "Chili: " + chiliCheckBox.isSelected() + ", " + "MeatFeast: " + meatFeastCheckBox.isSelected()
                        + ", " + "VeggieSpecial: " + veggieSpecialCheckBox.isSelected() + ", " + sumTextField.getText() + "\r\n");

                myWriter.close();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, e.toString(), "Alert", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_newFormSaveButtonActionPerformed

